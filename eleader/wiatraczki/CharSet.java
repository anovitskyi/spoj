package eleader.wiatraczki;

public class CharSet
{
   private char back;
   private char pen;
   
   public CharSet(char back, char pen)
   {
       this.back = back;
       this.pen = pen;
   }

    public char getBack()
    {
        return back;
    }

    public char getPen() 
    {
        return pen;
    }
   
   
}
